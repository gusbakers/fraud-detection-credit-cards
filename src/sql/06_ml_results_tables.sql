USE SCHEMA ML_RESULTS;

CREATE OR REPLACE TABLE PREDICTIONS (
    PREDICTION_ID       VARCHAR       DEFAULT UUID_STRING(),
    TRANSACTION_IDX     INTEGER,
    TRUE_LABEL          INTEGER,
    PREDICTED_LABEL     INTEGER,
    FRAUD_PROBABILITY   FLOAT,
    IS_CORRECT          BOOLEAN,
    MODEL_NAME          VARCHAR,
    MODEL_VERSION       VARCHAR,
    PREDICTION_TS       TIMESTAMP     DEFAULT CURRENT_TIMESTAMP()
);

CREATE OR REPLACE TABLE MODEL_METRICS (
    EXPERIMENT_ID       VARCHAR       DEFAULT UUID_STRING(),
    MODEL_NAME          VARCHAR,
    ROC_AUC             FLOAT,
    AVG_PRECISION       FLOAT,
    F1_SCORE            FLOAT,
    PRECISION_SCORE     FLOAT,
    RECALL_SCORE        FLOAT,
    TRAINING_ROWS       INTEGER,
    FEATURE_COUNT       INTEGER,
    CREATED_AT          TIMESTAMP     DEFAULT CURRENT_TIMESTAMP(),
    NOTES               VARCHAR
);
